    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        char var = 0;
        if(rbtMasculino.isSelected()){
            var='M';
        }
        if(rbtFemenino.isSelected()){
            var='F';
        }
        /*
        Validar que los campos sean rellenados para poder agregar personas.
        */
        TPersona.ingresarPersona(new Persona(1, 
                Integer.parseInt(txtRUT.getText()) ,
                txtNombre.getText(), 
                txtApellidoP.getText(), 
                txtApellidoM.getText(),
                Integer.parseInt(txtTelefono.getText()) ,
                txtDireccion.getText(), 
                txtCorreo.getText(),
                var,
                Integer.parseInt(txtEdad.getText())));
    }//GEN-LAST:event_btnAceptarActionPerformed

